package me.klez.authserver.model;

import jakarta.persistence.Column;
import jakarta.persistence.Embeddable;
import lombok.Getter;
import lombok.Setter;

import java.io.Serializable;
import java.util.Objects;

@Getter
@Setter
@Embeddable
public class Oauth2ClientRoleId implements Serializable {
	@Column(name = "client_id")
	private String clientId;
	@Column(name = "role_code")
	private String roleCode;

	@Override
	public boolean equals(Object o) {
		if (this == o) {
			return true;
		}
		if (o == null || getClass() != o.getClass()) {
			return false;
		}
		Oauth2ClientRoleId that = (Oauth2ClientRoleId) o;
		return Objects.equals(clientId, that.clientId) && Objects.equals(roleCode, that.roleCode);
	}

	@Override
	public int hashCode() {
		return Objects.hash(clientId, roleCode);
	}
}
